/**
 * Run this script ONCE while the backend is up to populate fallback data.
 * Usage: NEXT_PUBLIC_API_BASE_URL=https://api.example.com node scripts/cache-fallback.mjs
 *
 * Output: src/data/fallback/index.ts
 */

import { writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')

const BASE_URL = process.env.NEXT_PUBLIC_API_BASE_URL
if (!BASE_URL) {
  console.error('Error: NEXT_PUBLIC_API_BASE_URL env var is required')
  process.exit(1)
}

const LOCALES = ['en', 'ar']

const ENDPOINTS = [
  'home-page',
  'news',
  'about-page',
  'contact-page',
  'services-page',
  'how-it-works-page',
  'our-network-page',
  'careers-page',
  'visit-saudi-page',
  'resources-page',
  'cookies-page',
  'privacy-page',
  'terms-page',
  'case-studies',
  'blogs',
  'faqs',
  'guides',
  'webinars',
  'certifications',
  'concierge-page',
  'csr-page',
  'businesses-page',
  'individuals-page',
  'organizations-page',
  'services/business',
  'services/healthcare',
  'services/concierge',
]

async function fetchEndpoint(endpoint, locale) {
  const sep = endpoint.includes('?') ? '&' : '?'
  const url = `${BASE_URL}/api/${endpoint}${sep}locale=${locale}`
  try {
    const res = await fetch(url, {
      headers: { 'Content-Type': 'application/json' },
    })
    if (!res.ok) {
      console.warn(`  [${locale}] ${endpoint} → ${res.status} (skipped)`)
      return null
    }
    const json = await res.json()
    console.log(`  [${locale}] ${endpoint} → OK`)
    return json.data ?? null
  } catch (err) {
    console.warn(`  [${locale}] ${endpoint} → ERROR: ${err.message}`)
    return null
  }
}

async function main() {
  console.log(`Caching fallback data from ${BASE_URL} ...\n`)

  const result = {}

  for (const endpoint of ENDPOINTS) {
    result[endpoint] = {}
    for (const locale of LOCALES) {
      result[endpoint][locale] = await fetchEndpoint(endpoint, locale)
    }
  }

  const outDir = join(ROOT, 'src', 'data', 'fallback')
  mkdirSync(outDir, { recursive: true })

  const outFile = join(outDir, 'index.ts')
  const content = `// Auto-generated by scripts/cache-fallback.mjs — do not edit manually.
// Re-run the script while the backend is up to refresh this data.
// eslint-disable-next-line @typescript-eslint/no-explicit-any
const fallbacks: Record<string, Record<string, any>> = ${JSON.stringify(result, null, 2)}

export default fallbacks
`
  writeFileSync(outFile, content, 'utf8')
  console.log(`\nWrote ${outFile}`)
  console.log('Done. Rebuild the project to bundle the new fallback data.')
}

main()
